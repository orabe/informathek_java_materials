package JavaMaterials.kapitel1;

public class Variablen_2 {
    public static void main(String[] args) {
        // JAVA VARIABLEN:
        // Eine Variable ist ein Speicherort im Speicher, um Daten aufzubewahren.

        // Create Variables in Java
        int speedLimit = 80;

        // Change values of variables
        int limit;
        limit = 90;
        limit = 100;

        // Rules for Naming Variables in Java
        int age = 24;
        int AGE = 25;

        System.out.println(age);  // prints 24
        System.out.println(AGE);  // prints 25

        int alter;  // valid name and good practice
        int _alter;  // valid but bad practice
        int $alter;  // valid but bad practice

        // invalid variables
        // int 1age;
        // int my age;

    }
}
